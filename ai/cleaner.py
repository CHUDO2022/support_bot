        ("„Äê6:0‚Ä†addresses.txt„Äë –ê–¥—Ä–µ—Å —Å–∫–ª–∞–¥–∞", "–ê–¥—Ä–µ—Å —Å–∫–ª–∞–¥–∞"),
        
        # –ú–Ω–æ–∂–µ—Å—Ç–≤–µ–Ω–Ω—ã–µ –º–µ—Ç–∫–∏
